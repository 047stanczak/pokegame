package com.stanczak.pokegame.model;

public class Status {
    int attack;
    int defense;
    int speed;
    int sp_attack;
    int sp_defense;
    
    public int getAttack() {
        return attack;
    }
    public void setAttack(int attack) {
        this.attack = attack;
    }
    public int getDefense() {
        return defense;
    }
    public void setDefense(int defense) {
        this.defense = defense;
    }
    public int getSpeed() {
        return speed;
    }
    public void setSpeed(int speed) {
        this.speed = speed;
    }
    public int getSp_attack() {
        return sp_attack;
    }
    public int getSp_defense() {
        return sp_defense;
    }
}
